package org.pursuit;

public class PrimitiveConverter {
    public int intToInteger(int num) {
        return Integer.valueOf(num);
    }

    public double doubleToDouble(double num02) {
        return Double.valueOf(num02);
    }

    public char charToCharacter(char character) {
        return Character.valueOf(character);

    }

    public boolean booleanToBoolean(boolean name) {
        return Boolean.valueOf(name);
    }

}
